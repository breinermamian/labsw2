    @Override
    public void actualizar(AModel amodel) {
       try {
            GestorClientes gestor = (GestorClientes) amodel;
            ArrayList<Cliente> listado = gestor.consultarClientes();
            Graficar(listado);
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(GUIClientesVer.class.getName()).log(Level.SEVERE, null, ex);
            Utilidades.mensajeError("Error al consultar clientes", "Atenci√≥n");
        }
    }
    private void Graficar(ArrayList<Cliente> clientes){
        int width=40;
         int top=20;
         int left=100;
         int height=200;
         int desp=100;
         int totalHombres=0;
         int totalMujeres=0;
         
         
         for(int i=0;i<clientes.size();i++){
             
            if(clientes.get(i).getSexo().equals("Masculino")){
                totalHombres=totalHombres+1;
            }
            else{
                totalMujeres=totalMujeres+1;
            }
         }
         
         Graphics g = jPanel1.getGraphics();
         
         g.setColor(Color.white);
         g.fillRect(0, 0, this.getSize().width, this.getSize().height);
         
         g.setColor(Color.blue);
         g.drawRect(top, 100-(totalHombres*10),100 ,totalHombres*10);
         
         g.setColor(Color.black);
         float x = (float)100/(totalHombres+totalMujeres)*totalHombres;
         DecimalFormat df = new DecimalFormat("#.##"); 
         String mensaje1="HOMBRES "+df.format(x)+"%";
         g.drawString(mensaje1, top,100+20);
         //MUJERES
         g.setColor(Color.red);
         g.drawRect(top+120, 100-(totalMujeres*10),100, totalMujeres*10);
         g.setColor(Color.black);
         float y = (float)100/(totalHombres+totalMujeres)*totalMujeres;
         String mensaje2="MUJERES "+df.format(y)+"%";
         g.drawString(mensaje2, top+120,100+20);
        
    }
